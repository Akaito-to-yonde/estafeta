-- =============================================================================
-- SECTION 1.1 — CHECK CONSTRAINTS & pg_net EXTENSION
-- =============================================================================

-- Enable the pg_net extension for asynchronous HTTP calls from triggers
CREATE EXTENSION IF NOT EXISTS pg_net;

-- CHECK constraint: profile.categoria
-- Ensures only valid user categories are stored
ALTER TABLE profile
  ADD CONSTRAINT IF NOT EXISTS chk_profile_categoria
  CHECK (categoria IN ('admin', 'producer', 'provider', 'services', 'client'));

-- CHECK constraint: meeting.status
-- Ensures only valid meeting statuses are stored
ALTER TABLE meeting
  ADD CONSTRAINT IF NOT EXISTS chk_meeting_status
  CHECK (status IN ('proposed', 'accepted', 'rejected', 'rescheduled', 'cancelled'));


-- =============================================================================
-- SECTION 1.2 — CREATE notification_log TABLE
-- =============================================================================

-- Stores failed Edge Function notification attempts for observability and retry audits.
-- meeting_id and profile_id are bigint to match the bigint PKs of meeting and profile.
CREATE TABLE IF NOT EXISTS notification_log (
  id            bigint      generated by default as identity not null,
  created_at    timestamptz DEFAULT now(),
  function_name text        NOT NULL,
  recipient     text        NOT NULL,
  error         text        NOT NULL,
  meeting_id    bigint      REFERENCES meeting(id) ON DELETE SET NULL,
  profile_id    bigint      REFERENCES profile(id) ON DELETE SET NULL
);


