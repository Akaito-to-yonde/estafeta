-- ---------------------------------------------------------------------------
-- notify_on_new_registration()
-- Fires AFTER INSERT ON profile.
-- Calls the on-new-registration Edge Function via pg_net so the admin is
-- notified of every new pending registration.
-- ---------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION notify_on_new_registration()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
BEGIN
  PERFORM net.http_post(
    url     := (SELECT decrypted_secret FROM vault.decrypted_secrets WHERE name = 'SUPABASE_URL') || '/functions/v1/on-new-registration',
    headers := jsonb_build_object(
      'Content-Type',  'application/json',
      'Authorization', 'Bearer ' || (SELECT decrypted_secret FROM vault.decrypted_secrets WHERE name = 'SUPABASE_FUNCTION_KEY')
    ),
    body    := jsonb_build_object(
      'profile_id',     NEW.id,
      'email',          NEW.email,
      'categoria',      NEW.categoria,
      'nombre_empresa', NEW.nombre_empresa
    )
  );
  RETURN NEW;
END;
$$;

