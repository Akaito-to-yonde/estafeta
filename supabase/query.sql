-- =============================================================================
-- SECTION 1.5 — ATTACH TRIGGERS TO TABLES
-- =============================================================================
-- DROP + CREATE used for idempotency since CREATE OR REPLACE TRIGGER requires
-- the trigger to already exist on older Postgres versions. Supabase runs
-- Postgres 15+ so CREATE OR REPLACE TRIGGER is supported, but DROP IF EXISTS
-- first is equally safe and more portable.

-- ---------------------------------------------------------------------------
-- Profile triggers
-- ---------------------------------------------------------------------------

DROP TRIGGER IF EXISTS trg_new_registration ON profile;
CREATE TRIGGER trg_new_registration
  AFTER INSERT ON profile
  FOR EACH ROW
  EXECUTE FUNCTION notify_on_new_registration();

DROP TRIGGER IF EXISTS trg_approval_invite ON profile;
CREATE TRIGGER trg_approval_invite
  AFTER UPDATE OF estado ON profile
  FOR EACH ROW
  WHEN (NEW.estado = 'approved')
  EXECUTE FUNCTION notify_on_approval_invite();

DROP TRIGGER IF EXISTS trg_rejection ON profile;
CREATE TRIGGER trg_rejection
  AFTER UPDATE OF estado ON profile
  FOR EACH ROW
  WHEN (NEW.estado = 'rejected')
  EXECUTE FUNCTION notify_on_rejection();

