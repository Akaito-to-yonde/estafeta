-- =============================================================================
-- SECTION 1.7 — RLS POLICIES: profile TABLE
-- =============================================================================

ALTER TABLE profile ENABLE ROW LEVEL SECURITY;

-- SELECT: users see only their own row; admins see all rows;
-- anonymous users can see pending profiles (user_id IS NULL) so the login
-- form can distinguish "not registered" from "pending approval".
-- Once the invite is generated and user_id is linked, the row is no longer
-- visible to anonymous users — only to the authenticated owner.
DROP POLICY IF EXISTS profile_select ON profile;
CREATE POLICY profile_select ON profile
  FOR SELECT
  USING (
    (user_id = auth.uid())
    OR ((auth.jwt() -> 'app_metadata' ->> 'categoria') = 'admin')
    OR (user_id IS NULL)
  );

-- INSERT: open to unauthenticated requests so new users can self-register
DROP POLICY IF EXISTS profile_insert ON profile;
CREATE POLICY profile_insert ON profile
  FOR INSERT
  WITH CHECK (true);

-- UPDATE: users update only their own row; admins can update any row
DROP POLICY IF EXISTS profile_update ON profile;
CREATE POLICY profile_update ON profile
  FOR UPDATE
  USING (
    (user_id = auth.uid())
    OR ((auth.jwt() -> 'app_metadata' ->> 'categoria') = 'admin')
  );

-- DELETE: admins only (used when rejecting a registration)
DROP POLICY IF EXISTS profile_delete ON profile;
CREATE POLICY profile_delete ON profile
  FOR DELETE
  USING (
    (auth.jwt() -> 'app_metadata' ->> 'categoria') = 'admin'
  );


-- =============================================================================
-- SECTION 1.8 — RLS POLICIES: conference AND meeting TABLES
-- =============================================================================

-- ---------------------------------------------------------------------------
-- conference table
-- ---------------------------------------------------------------------------

ALTER TABLE conference ENABLE ROW LEVEL SECURITY;

-- SELECT: public read — conferences are visible to everyone including anon users
DROP POLICY IF EXISTS conference_select ON conference;
CREATE POLICY conference_select ON conference
  FOR SELECT
  USING (true);

-- INSERT: only the authenticated user who will be the speaker
DROP POLICY IF EXISTS conference_insert ON conference;
CREATE POLICY conference_insert ON conference
  FOR INSERT
  WITH CHECK (speaker_id = auth.uid());

-- UPDATE: host users may only edit their own conferences
DROP POLICY IF EXISTS conference_update ON conference;
CREATE POLICY conference_update ON conference
  FOR UPDATE
  USING (speaker_id = auth.uid());

-- DELETE: the owning speaker, or an admin
DROP POLICY IF EXISTS conference_delete ON conference;
CREATE POLICY conference_delete ON conference
  FOR DELETE
  USING (
    (speaker_id = auth.uid())
    OR ((auth.jwt() -> 'app_metadata' ->> 'categoria') = 'admin')
  );

-- ---------------------------------------------------------------------------
-- meeting table
-- ---------------------------------------------------------------------------

ALTER TABLE meeting ENABLE ROW LEVEL SECURITY;

-- SELECT: host, client, or admin can read a meeting row
DROP POLICY IF EXISTS meeting_select ON meeting;
CREATE POLICY meeting_select ON meeting
  FOR SELECT
  USING (
    (speaker_id     = auth.uid())
    OR (participant_id = auth.uid())
    OR ((auth.jwt() -> 'app_metadata' ->> 'categoria') = 'admin')
  );

-- INSERT: only the client (participant) may propose a meeting
DROP POLICY IF EXISTS meeting_insert ON meeting;
CREATE POLICY meeting_insert ON meeting
  FOR INSERT
  WITH CHECK (participant_id = auth.uid());

-- UPDATE: either party (host or client) may update the meeting during negotiation
DROP POLICY IF EXISTS meeting_update ON meeting;
CREATE POLICY meeting_update ON meeting
  FOR UPDATE
  USING (
    (speaker_id     = auth.uid())
    OR (participant_id = auth.uid())
  );
